//Arrow Functions





const sumar2 = function(n1, n2) {

    console.log(n1 + n2);
}

sumar2(5, 10);


//SINTAXIS ARROW FUNCTIONS

const sumar3 = (n1, n2) => {
    console.log(n1 * n2)
}

sumar3(2, 3);

const aprendiendo = (tecnologia) => {
    console.log(`Aprendiendo ${tecnologia}`);

}

